import { TransactionalEmailsApi, TransactionalEmailsApiApiKeys } from "@getbrevo/brevo";

// ‚úÖ Email sender function
async function sendStudentCredentialsEmail(student: any) {
  try {
    const client = new TransactionalEmailsApi();
    client.setApiKey(TransactionalEmailsApiApiKeys.apiKey, process.env.BREVO_API_KEY!);

    const emailData = {
      sender: { email: "ahmadwebcrafts@gmail.com", name: "Quran Academy" },
      to: [{ email: student.email }],
      subject: "Your Student Account Credentials",
      htmlContent: `
        <div style="font-family: Arial, sans-serif; line-height: 1.5;">
          <h2>Welcome ${student.name}</h2>
          <p>Your student account has been created. Here are your credentials:</p>
          <p><b>Educational Email:</b> ${student.educationMail}</p>
          <p><b>User ID:</b> ${student.userId}</p>
          <p><b>Password:</b> ${student.password}</p>
          <p>Please keep this information safe.</p>
        </div>
      `,
    };

    const result = await client.sendTransacEmail(emailData);
    console.log(`‚úÖ Email sent to ${student.email}:`, result);
  } catch (err: any) {
    console.warn(`‚ö†Ô∏è Failed to send email to ${student.email}:`, err?.response?.body || err);
  }
}

// ‚úÖ Bulk email sender
async function sendBulkStudentEmails(students: any[]) {
  for (const student of students) {
    sendStudentCredentialsEmail(student); // don't await to avoid blocking
  }
}

export async function POST(req: Request) {
  try {
    await connectToDB();
    const body = await req.json();

    // üü¢ Case 1: Bulk insert
    if (body.students && Array.isArray(body.students)) {
      const { parentInquiry, students } = body;

      if (!parentInquiry || students.length === 0) {
        return NextResponse.json(
          { error: "parentInquiry and students[] are required" },
          { status: 400 }
        );
      }

      const autoGenerated = await generateSequentialIdsAndMails(students);

      const docs = students.map((s: any, idx: number) => ({
        parentInquiry,
        name: s.name,
        email: s.email,
        phone: s.phone,
        gender: s.gender,
        dateOfBirth: s.dateOfBirth ? new Date(s.dateOfBirth) : null,
        timezone: s.timezone,
        preferredStartDate: s.preferredStartDate || null,
        preferredStartTime: s.preferredStartTime,
        classDays: s.classDays,
        course: s.course || null,
        price: Number(s.price),
        status: "trial",
        trialClasses: { assigned: 3, completed: 0 },
        feeStatus: { paid: false },
        userId: autoGenerated[idx].userId,
        educationMail: autoGenerated[idx].educationMail,
        password: autoGenerated[idx].password,
      }));

      const created = await Student.insertMany(docs);

      // ‚úÖ Send bulk emails asynchronously
      sendBulkStudentEmails(created);

      return NextResponse.json(created, { status: 201 });
    }

    // üü¢ Case 2: Single student
    const {
      name,
      email,
      phone,
      gender,
      dateOfBirth,
      timezone,
      preferredStartDate,
      preferredStartTime,
      classDays,
      course,
      price,
      parentInquiry,
      password: incomingPassword,
    } = body;

    if (!name || !email || !phone || !gender || !timezone || !price || !dateOfBirth) {
      return NextResponse.json({ error: "Required fields are missing" }, { status: 400 });
    }

    const userId = await generateUserId();
    const educationMail = generateEducationMailFromUserId(userId, name);
    const password = incomingPassword || generatePassword(name);

    const student = new Student({
      parentInquiry: new mongoose.Types.ObjectId(parentInquiry),
      name,
      email,
      phone,
      gender,
      dateOfBirth: new Date(dateOfBirth),
      timezone,
      preferredStartDate: preferredStartDate || null,
      preferredStartTime,
      classDays,
      course,
      price,
      status: "trial",
      trialClasses: { assigned: 3, completed: 0 },
      feeStatus: { paid: false },
      userId,
      educationMail,
      password,
    });

    await student.save();

    // ‚úÖ Send email asynchronously
    sendStudentCredentialsEmail(student);

    return NextResponse.json(student, { status: 201 });
  } catch (error: any) {
    console.error("‚ùå Error in POST /api/db/students:", error);
    return NextResponse.json({ error: error.message }, { status: 500 });
  }
}
